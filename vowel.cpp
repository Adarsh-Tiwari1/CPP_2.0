
#include<iostream>
using namespace std;
int main(){
    char ch;
    cout<<"enter character\n";
    cin>>ch;
    if(ch=='a'){
        cout<<"this is vowel";
    }
    else if(ch=='e'){
        cout<<"this is vowel";
    }
    else if(ch=='i'){
        cout<<"this is vowel";
    }
    else if(ch=='o'){
        cout<<"this is vowel";
    }
    else if(ch=='u'){
        cout<<"this is vowel";
    }
    else{
        cout<<"this is consonant";
    }
}

